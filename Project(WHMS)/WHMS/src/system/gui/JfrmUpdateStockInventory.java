    private void btnAdStkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdStkActionPerformed
        String whNo=(String)cmbWhNo.getSelectedItem();
        String matNo=(String)cmbMatNo.getSelectedItem();
        double qty=0, ordLvl=0, maxQty=0, exStk=0;
        String oLvl=txtOrdLvl.getText();
        String mQty=txtMaxQty.getText();
        Stock stk=null;
        Warehouse wh=new Warehouse(whNo);
        ResultSet rs=null;
        boolean updt=false;
        int cnfm;
        
        if(oLvl.isEmpty() | mQty.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Feilds Order Level & Max Quantity can't be left blank when updating" , "Validation Issue", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
              try{
                  ordLvl=Double.parseDouble(oLvl);
                  maxQty=Double.parseDouble(mQty);
              }
              catch(NumberFormatException ex)
              {
                  JOptionPane.showMessageDialog(null, "Please enter Numeric values for Order Level & Maximum Quantity and retry :", "Validation Issue", JOptionPane.WARNING_MESSAGE);
                  return;
              }
        }
             
        if(ordLvl<1000 | maxQty<1000)
        {
            JOptionPane.showMessageDialog(null, "Order Level & Maximum Quantity can't be lesser than 1000" , "Validation Issue", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            stk=new Stock(whNo, matNo);
            try{
                
                wh.pullWhDtls();
                exStk=stk.pullExStock();
                
                if(wh.getStrgSpc()>(maxQty+exStk))
                {
                    cnfm=JOptionPane.showConfirmDialog(null,"Are you sure you want to Add this Stock", "Confirm Stock Add", JOptionPane.INFORMATION_MESSAGE);

                    if(cnfm==JOptionPane.YES_OPTION)
                    {
                        stk=new Stock(whNo, matNo, qty, ordLvl, maxQty);

                        updt=stk.insertStock();
                        

                        if(updt==true)
                        {
                            JOptionPane.showMessageDialog(null, "Stock Inventory Updated Successfully!", "Update Successful", JOptionPane.INFORMATION_MESSAGE);
                            cmbWhNo.setSelectedIndex(0);
                            txtMaxQty.setText(null);
                            txtOrdLvl.setText(null);
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(null, "Stock Inventory Update Failed!", "Update Failed", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
                
                else
                {
                    JOptionPane.showMessageDialog(null, "The Maximum Storage space requested can't be allocated in the warehouse "
                            + "\nWareHouse Capacity: " + wh.getStrgSpc() + "\nCapacity already utilized: " + exStk 
                            + "\nCapasity Required to Allocate Stock: " + (maxQty+exStk), 
                            "Storage space Issue", JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(SQLException | ClassNotFoundException ex){
                JOptionPane.showMessageDialog(null, "Oops Something went Wrong :" + ex.getMessage(), "Runtime Exception", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        
    }//GEN-LAST:event_btnAdStkActionPerformed

