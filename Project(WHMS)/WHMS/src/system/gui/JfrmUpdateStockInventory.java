    private void cmbWhNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbWhNoActionPerformed
        
        String whNo=(String)cmbWhNo.getSelectedItem();
        List<String> allMatOrg=new ArrayList<>();
        List<String> allMatChg=new ArrayList<>();
        
        cmbMatNo.removeAllItems();
        
        boolean avl=false;
        int indx=0;
        
        try{
            allMatOrg=Material.listMaterial();
            //JOptionPane.showMessageDialog(null, "Step1" );
            
            for(String s: allMatOrg)
            {
               avl=Stock.isAvailable(whNo,s);
               //JOptionPane.showMessageDialog(null, "Step2" );
               if(avl==false)
               {
                   allMatChg.add((String)allMatOrg.get(indx));
                   //JOptionPane.showMessageDialog(null, "Step3" );
               }
               
               avl=false;
               indx++;
            }
        }
        catch(SQLException | ClassNotFoundException ex)
        {
            JOptionPane.showMessageDialog(null, "Oops Something went Wrong :" + ex.getMessage(), "Runtime Exception", JOptionPane.ERROR_MESSAGE);
        }
        
        allMatChg.forEach((s)->{
            cmbMatNo.addItem(s);
        });
    }//GEN-LAST:event_cmbWhNoActionPerformed

